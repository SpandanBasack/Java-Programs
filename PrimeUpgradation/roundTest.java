public class roundTest
{
    public static void main(String[]args)
    {
        System.out.println("3.5 is rounded to: "+Math.round(3.5));
        System.out.println("3.4 is rounded to: "+Math.round(3.4));
        System.out.println("-3.5 is rounded to: "+Math.round(-3.5));
        System.out.println("-3.1 is rounded to: "+Math.round(-3.1));
        System.out.println("-3.9 is rounded to: "+Math.round(-3.9));
    }
}